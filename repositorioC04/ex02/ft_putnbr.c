#include <unistd.h>

void	ft_putnbr(int nb)
{
	char	num[11];
	int		i;

	i = 0;
	if (nb == -2147483648)
		write (1, "-2147483648", 11);
	else
	{
		if (nb < 0)
		{
			write (1, "-", 1);
			nb = -nb;
		}
		while (nb > 0)
		{
			num[i++] = (nb % 10) + '0';
			nb = nb / 10;
		}
		if (i == 0)
			num[i++] = '0';
		while (i > 0)
			write (1, &num[--i], 1);
	}
}
