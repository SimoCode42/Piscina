#include <unistd.h>

void	ft_print_comb2(void)
{
	char	a;
	char	b;
	char	c;

	a = 0;
	while (a < 100)
	{
		b = a + 1;
		while (b < 100)
		{
			c = '0' + a / 10;
			write(1, &c, 1);
			c = '0' + a % 10;
			write(1, &c, 1);
			write(1, " ", 1);
			c = '0' + b / 10;
			write(1, &c, 1);
			c = '0' + b % 10;
			write(1, &c, 1);
			if (!((a == 98) & (b == 99)))
				write(1, ", ", 2);
			b++;
		}
		a++;
	}
}
