#include <unistd.h>

unsigned int	ft_strlcpy(char *dest, char *src, unsigned int size)
{
	unsigned int	p;
	unsigned int	i;

	p = 0;
	i = 0;
	while (src[p] != '\0')
		p++;
	if (size == 0)
		return (p);
	while (src[i] != '\0' && i < (size - 1))
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (p);
}
